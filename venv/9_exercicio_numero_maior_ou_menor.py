# Recaba um numero e exiba se ele é Positivo ou Negativo

numero = float(input("Digite o número para verificarmos: "))

if numero > 0: # Se >>> Se é maior que zero
    print("O número é Positivo!")

elif numero < 0: # Se não >>> O inverso de if
    print("O número é Negativo!")

else:
    print("O número é 0!") # Quando não é: Se (if) | Se não (elife)

