# for >> quando sei até onde quero ir


# DO 0 AO 10 

for i in range(0, 10):
    print(i)

# INTERVALO DE 5 EM 5

for i in range(0, 35, 5): # Coloquei o 5 no final, logo ele vai contar com intervalo de 5 em 5.
    print(i)


# ORDEM DECRESCENTE

for i in range(10, 0, -1):
    print(i)

# ITERAÇÃO

x = [2, 7, "x", "z", 45]

for i in x:
    print(i)

# SEPARAR LETRA POR LETRA

x = input("Digite o seu nome: ")

for i in x:
    print(i)

