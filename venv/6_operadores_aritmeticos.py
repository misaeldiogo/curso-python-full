import math

operacoes = 2 + 2
print(operacoes)

operacoes = 2 - 2
print(operacoes)

operacoes = 2 * 2.5  # Com um asteristico aceita float (numeros quebrado)
print(operacoes)

operacoes =  2 ** 2 # Com doias asteristicos não aceita numero quebrado float
print(operacoes)

operacoes =  50 % 20
print(operacoes)

operacoes = 10 / 2 # Com uma barra aceita float (numeros quebrado)
print(operacoes)

operacoes = 10 // 2  # Com duas barras não aceita numero quebrado float
print(operacoes)

operacoes = math.sqrt(25)  # Raiz quadrada >>> Precisa do Import Math
print(operacoes)