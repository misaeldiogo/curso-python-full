# and or not   >>  e ou não

# and >> Só é verdadeiro se ambos forem verdadeiro >> True

operador = True and True  # Verdadeiro / True
print(operador)

operador = True and False  # Falso / False
print(operador)

# or >> Para ser verdadeiro, basta que um seja verdade / True

operador = True or True  
print(operador)

operador = True or False  
print(operador)

# not >> Inverte a operação

operador = not True 
print(operador)

operador = not False  
print(operador)

